<script>
export default {
  name: 'Navbar',
  data: () => ({
    loading: false,
    timestamp: new Date().getTime(),
    interval: null
  }),
  computed: {
    user() {
      return this.$store.getters.info
    },
    isSession() {
      return Object.keys(this.$store.getters.session).length > 0
    },
    session() {
      return this.$store.getters.session
    },
    isSessionOwn() {
      return this.$store.getters.info.uid === this.$store.getters.session.uid
    },
    theme() {
      return this.$store.getters.theme
    }
  },
  methods: {
    async logout() {
      if (this.isSession && this.isSessionOwn) {
        this.$toast.default('Сначала закройте смену')
      } else {
        const res = await this.$dialog.confirm({
          title: 'Вы хотите выйти?',
          icon: 'warning',
          cancelButtonText: 'Отмена'
        })
        if (res.isOk) {
          await this.$store.dispatch('logout')
          this.$router.push('/login')
        }
      }
    },
    toggleTheme() {
      this.$store.dispatch('toggleTheme')
    }
  },
  mounted() {
    this.interval = setInterval(() => {
      this.timestamp = new Date().getTime()
    }, 1000)
  },
  beforeDestroy() {
    clearInterval(this.interval)
  }
}
</script>
